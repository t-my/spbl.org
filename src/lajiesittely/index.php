          <h1>Lajiesittely - mitä paintball on?</h1>

          <p>
          Paintball on harrastus joka sopii kaikille ikään tai sukupuoleen katsomatta. Fyysinen voima tai koko eivät ole tärkeitä, vaan paremmuus ratkaistaan taktikoinnilla ja nopealla päättelykyvyllä; kyky tehdä oikeita päätäksiä nopeasti on olennainen menestymisen edellytys, samoin kuin taito tyäskennellä tiimissä ja tulla toimeen erilaisten ihmisten kanssa. Voit unohtaa päivittäisen stressin hetkeksi ja siirtyä kokonaan toiseen maailmaan, joka ei tunne sosiaalisia rajoja eikä arkisia murheita.
          </p>

          <h2>Historiaa</h2>


          <p>Paintball sai alkunsa 1970-luvulla, jolloin kehitettiin ensimmäiset värikuulamerkkaimet. Merkkaimet oli suunniteltu metsureiden ja karjankasvattajien avuksi puiden ja eläimien merkkaamiseen. Tuohon aikaan maalit eivät olleet vesiliukoisia eivätkä muutenkaan tarkoitettuja minkäänlaiseen harrastekäyttään.</p>

          <p>Ensimmäinen paintball-peli syntyi vuonna 1981, jolloin Hayes Noel ja Charles Gaines ideoivat miten pystyttäisiin mittaamaan kenellä on paras kilpailutilanteessa tarvittava hiiviskely- ja paineensietokyky sekä tiukassa paikassa tarvittava kylmähermoisuus. Heidän kaverinsa George Butler näki eräässä maatalouslehdessä Nelspot 007 -merkkaimen mainoksen ja kolmikko päätti järjestää pelin testatakseen kuka heistä on taitavin.
          <div>
          <img src="/images/nespot.jpg" style="width: 50%; padding:2%; display: inline-block; float: right" />
          <i>Nelson</i>
          </div>
          Ensimmäisen paintball-pelin pelialue oli noin puolen neliäkilometrin kokoinen ja peliin osallistui kaksitoista pelaajaa. Pisteitä sai pelaajien merkkaamisesta sekä eri puolille pelialuetta sijoitettujen lippujen kotiuttamisesta. Peliaika oli 24 tuntia.Ensimmäisen pelin voitti herra nimeltään Ritchie White, joka oli vanha eränkävijä. Häntä ei nähty koko pelin aikana eikä hän ampunut ensimmäistäkään kuulaa vaan keräsi liput välttäen toisia pelaajia.
          </p>

          <p>Lakimiehet neuvoivat pelinkehittäjiä hakemaan patenttia kehittämälleen pelille, mutta he päättivät olla patentoimatta peliä. Lakimiehet pitivät tätä aivan hulluna ideana, mutta kehittäjät pitivät parempana omistaa pienen palan isosta kakusta kuin ison palan pienestä kakusta. Tämä on varmasti yksi syy, miksi paintball on kasvanut näin nopeasti.</p>

          <p>Suomen paintball saavutti 80-luvun puolessa välissä, jolloin 12 gramman hiilidioksidikapseleilla toimivia merkkaimia tuotiin Suomeen ulkomailta. Ensimmäinen suomenmestaruus ratkottiin vuonna 1990 Vääksyssä 10-henkisin joukkuein. Turnauksen voitti joukkue nimeltään Splatfighters.</p>

          <h2>Miten paintballia pelataan nykyisin?</h2>

          <p>Pelissä jokaisella pelaajalla on merkkain, ja jokainen pelaaja joka saa osuman on eliminoitu pelistä. Peliä voi pelata joko harrastemielessä hieman epävirallisemmin tai virallisesti kisapaintballina. Yleensä harrastepeleissä pelataan erilaisia lipunryästäpelin variaatioita. Tässä pelimuodossa 3-10 pelaajaa yrittää ryästää toisen joukkueen lipun ja eliminoida toisen joukkueen pelaajat. Se joukkue, joka saa vietyä kentän keskellä olevan lipun vastustajan tukikohtaan tai eliminoimaan kaikki toisen joukkueen pelaajat, on voittaja.</p>

          <p>Peliä pelataan joko ulkona rajatulla alueella tai halleihin tehdyillä sisäkentillä. Pelikentän koko vaihtelee pelaajien määrästä ja pelityypistä riippuen. Kentille on tehty keinotekoisia suojia ja esteitä, yleisimmin tynnyreitä ja vaneri- tai lastulevyjä. Supair-kentät, missä on ilmalla täytettyjä suojia, ovat yleistymässä, ja niitä käytetäänkin tätä nykyä käytännässä jokaisessa Suomessa järjestettävässä turnauksessa. Supair -kentät ovat helppoja liikutella paikasta toiseen ja ne ovat nopeasti pelikunnossa. Näiden kenttien ansiosta paintball on nopeasti tullut metsistä kaupunkien urheilukentille suuren yleisän tietoisuuteen. Tavallinen metsäkin kelpaa pelikentäksi mikäli luvat maanomistajalta ovat kunnossa.</p>

          <p>Suomessa kisapaintballia pelataan tasoilla SM-liigasta 3.divisioonaan ja erilaisia turnauksia järjestetään vuosittain noin kymmenkunta. SPBL:n alaisissa sarjoissa pelaa kaudella 2012 yhteensä noin 40 joukkuetta.</p>

