          <h2>Varusteet</h2>

          <p>Pelaamiseen tarvittavat varusteet ovat merkkain, loaderi, paineilmapullo, värikuulat sekä maski. Kaikki muu on enemmän tai vähemmän tarpeellista - joskin kohtalaisen mukavaa olla. Merkkaimia on pääsääntäisesti kahta erityyyppiä: sähkäisiä ja mekaanisia. Yleisimmin vuokrauskäytässä olevat merkkaimet ovat mekaanisia ja siten erittäin kestäviä. Nykyään kaikki kisapaintballissa käytettävät merkkaimet ovat sähkäisiä ja ne pystyvät 10 palloa sekunnissa tulinopeuteen. Merkkainten hinta vaihtelee 150 euron ja 1500 euron välillä riippuen merkistä, mallista ja käyttätarpeesta.</p>

          <p>Loaderit ovat lippaita johon värikuulat laitetaan. Merkkaimen ammuttua yhden kuulan loaderista tiputetaan tai tyännetään seuraava kuula valmiiksi piippuun odottamaan laukaisua. Loadereita on kahta tyyppiä: painovoimaan perustuvia sekä sähkäisiä. Sähkäisillä loadereilla saavutetaan nopeampi syättä sekä parempi toimintavarmuus. Tyypilliseen loaderiin mahtuu noin 200 värikuulaa.</p>

          <p>Maski on yksittäisistä varusteista selkeästi tärkein. Se suojelee silmiä ja kasvoja kuulien osumilta. Kuulat lähtevät merkkaimen piipun suulta mitattuna noin 90 metriä / sekunnissa nopeudella joten tätä tietoa vasten on ymmärrettävää että silmiä tulee suojella. Hyvät maskit maksavat ostettuna noin 50-100e. Kaikkien ei-hyväksyttyjen maskien käyttä paintballissa on kiellettyä turvallisuussyistä. Käytä aina vain hyväksyttyjä paintballmaskeja.</p>

          <p>Merkkaimilla ammutaan .68 kaliberia olevia värikuulia. Kuulien kuori on gelatiinia eli liivatteena paremmin tunnettua ainetta. Kuulien sisällä on kasvirasvaa, joka on värjätty elintarvikeväreillä. Maali on vesiliukoista ja täysin vaaratonta, ja lähtee helposti tavallisessa pesussa kunhan värin ei anna jämähtää vaatteisiin päiviksi. Tästä huolimatta vitivalkoisia vaatteita ei suositella.<p>

          <p>Merkkaimen voimanlähteenä on yleisimmin paineilma. Paineilmapullot valmistetaan joko alumiinista tai kevlarista kevlar pullon ollessa kevyempi ja kalliimpi vaihtoehto. Yleisin paineilmapullon koko on 1.1l. Paineilmapullot voidaan täyttää aina uudelleen käytän jälkeen.</p>


          <p>Vaatteiksi käyvät mitkä tahansa vapaa-ajan vaatteet kuten verryttelyhousut ja hupparit. Värikuulien väri lähtee pesussa pois mutta vaaleimpiin vaatteisiin saattaa jäädä himmeitä tahroja, joten tummat vaatteet sopivat paremmin. Kilpapaintballissa  käytetään peliin suunniteltuja paitoja ja housuja, joissa on vahvistettu polvia ja kyynärpäitä. Metsäpeleissä taas maastopuvut ovat suosittuja niiden suojavärin ja kulutuskestävyyden takia.. Vaatteiden lisäksi on saatavina erilaisia suojia, kuten polvi ja kyynärsuojat jotka suojaavat polvien ja kyynärpäiden lisäksi käsivarsia ja sääriä. Nämä eivät ole pakollisia vaan lähinnä mukavuutta antamassa.</p>
